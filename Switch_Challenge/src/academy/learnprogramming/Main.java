package academy.learnprogramming;

public class Main {

    public static void main(String[] args) {

        char character = 'B';

        switch(character){

            case 'B':
                System.out.println("Is the char B");
                break;

            case 'C':
                System.out.println("Is the case C");
                break;

            case 'D':
                System.out.println("Is the case C");
                break;

            default:
                System.out.println("Was not 'A' 'B' or 'C'");


        }

    }
}
